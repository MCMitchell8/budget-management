    private void jTableTransactionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTransactionsMouseClicked
        
        //handles the user selecting a row in the table
        int numberOfRowsSelected = jTableTransactions.getSelectedRowCount();
        if (numberOfRowsSelected > 1) {
            clearDisplay();
            return;
        }
        
        int rowSelected = jTableTransactions.getSelectedRow();

        Transaction transaction = transactions.get(rowSelected);
        jComboBoxCategory.setSelectedItem(transaction.getCategory().toString());

        double transactionPrice;
        transactionPrice = transaction.getPrice();
        
        //sets text fields to the information from the selected row
        jTextFieldPrice.setText(String.format("%,.2f", transactionPrice));
        jComboBoxMonth.setSelectedItem(Conversions.convertMonthToString(transaction.getMonth()));
        jComboBoxYear.setSelectedItem(Integer.toString(transaction.getYear()));
        jComboBoxSingleOrRecurring.setSelectedItem(transaction.getRecurring() ? "Yes" : "No");
        jTextFieldNumberPerMonth.setText(Integer.toString(transaction.getEveryMonth()));
        
    }//GEN-LAST:event_jTableTransactionsMouseClicked

    
    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        
        //handles when the user presses the update button
        //checks how many rows are selected
        int rowCount = jTableTransactions.getSelectedRowCount();
        if (rowCount > 1) {
            //if more than one row is selected, informs user
            JOptionPane.showMessageDialog(null, "You can't update more then one row at a time.", "Invalid Update", 0);
            return;
        }
        
        //update entry
        int rowSelected = jTableTransactions.getSelectedRow();

        if (rowSelected == -1) {
            //if no rows are selected, exit
            return;
        }
        
        boolean updateFuture = false;
        
        //gets updated information
        Transaction transaction = getTransaction();
        
        //gets original information and removes data
        Transaction original = transactions.remove(rowSelected);
        
        //if selection acts on more than one transaction, ask the user what they want to do
        if (transaction.recurring || original.recurring) {
            String[] options = {"This One", "All Future", "Cancel"};
            int selection = JOptionPane.showOptionDialog(null, 
                    "Do you want to update only this transaction, or all future ones as well?", 
                    "Update Recurring Transaction", 
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null, 
                    options, 
                    options[0]);
           
            //if user decides to cancel, add the row back and exit
            if (selection == 2) {
                transactions.add(rowSelected, original);
                return;
                
            //if user decides to update all future selections, set flag to true
            } else if (selection == 1) {
                updateFuture = true;
            }
        }

        
        if (updateFuture) {
            //if the update flag is set and the original transaction is not 
            //recurring, create future transactions
            System.out.println("future me");
            if (!original.recurring) {
                System.out.println("Creating the future");
                transaction.createFuture(transactions);
            //otherwise the original is recurring
            } else {
                //if the new one is also recurring, undate the future transactions
                if (transaction.recurring) {
                    System.out.println("Updating the future");
                    transaction.updateFuture(transactions, original);
                    
                //otherwise delete the future transactions
                } else {
                    System.out.println("Destroying the future");
                    original.deleteFuture(transactions);
                }
            }
        }
        
        //make sure that the first transaction exists
        transactions.add(rowSelected, transaction);

        writeTransactions();
        clearDisplay();
        updateDisplay();
        
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    
    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed

        //get the number of selected rows
        int rowCount = jTableTransactions.getSelectedRowCount();
        
        //if there is more than one row while there are still rows, 
        //get the first selected row and deletes it
        if (rowCount > 1) {
            while (rowCount > 0) {
                int rowSelected = jTableTransactions.getSelectedRow();

                if (rowSelected >= 0) {
                    transactions.remove(rowSelected);
                    model.removeRow(rowSelected);
                }

                rowCount = jTableTransactions.getSelectedRowCount();
            } 
            
        //otherwise, determine if the transaction is recurring 
        } else {
            
            //get the currently selected row
            int rowSelected = jTableTransactions.getSelectedRow();
            
            //remove the transaction for the row
            Transaction transactionToDelete = transactions.remove(rowSelected);
            
            //if the transaction is recurring, ask the user what they want to do
            if (transactionToDelete.recurring) {
                String[] options = {"This One", "All Future", "Cancel"};
                int selection = JOptionPane.showOptionDialog(null, 
                        "Do you want to update only this transaction, or all future ones as well?", 
                        "Update Recurring Transaction", 
                        JOptionPane.DEFAULT_OPTION,
                        JOptionPane.QUESTION_MESSAGE,
                        null, 
                        options, 
                        options[0]);
                
                //if the user decides to cancel, add the transaction back and exit
                if (selection == 2) {
                    transactions.add(transactionToDelete);
                    return;
                    
                //if the user selects all future, delete the rest of the child transactions 
                } else if (selection == 1) {
                    transactionToDelete.deleteFuture(transactions);
                }
            }
        }
        
        writeTransactions();
        clearDisplay();
        updateDisplay();
        
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    
