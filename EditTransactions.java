    private Transaction getTransaction() {
        
        //gets transaction from user input form
        Category category = Category.CreateFromString(jComboBoxCategory.getSelectedItem().toString());
        
        String priceString = jTextFieldPrice.getText();
        if (priceString.isEmpty()) {
            priceString = "0";
        }
        double price = new Double(priceString);
        
        int month = Conversions.convertStringToMonth((String) jComboBoxMonth.getSelectedItem());
        int year = new Integer((String)jComboBoxYear.getSelectedItem());
        
        System.out.println(String.valueOf(month));
        
        boolean recurring;
        String recurringString = jComboBoxSingleOrRecurring.getSelectedItem().toString();
        if(recurringString == "Yes"){
            recurring = true;
        }
        else {
            recurring = false;
        }
        
        int everyMonth = new Integer(jTextFieldNumberPerMonth.getText());
        
        Transaction transaction = new Transaction(category, price, month, year, recurring, everyMonth);

        return transaction;
        
    }

        
