    //sorts and filters the data and updates the display
    public void updateDisplay() {
        
        int count = model.getRowCount();
        
        //Remove all rows from existing table
        for (int removeItem = 0; removeItem < count; removeItem++) {
            model.removeRow(0);
        }

        //Goes through transactions and separates them based on selection
        for(Transaction transaction : transactions){
            if (Filters.passesTransactionFilter(transaction, displayFilter) &&
                    Filters.passesCategoryFilter(transaction, 
                            filterCategories ? categoryFilter : null) &&
                    Filters.passesMonthFilter(transaction, monthToFilter) &&
                    Filters.passesYearFilter(transaction, yearToFilter)) {
                
                // If the transaction passes all the filters, add it to the model
                model.addRow(new Object[]{
                    Conversions.convertMonthToString(transaction.getMonth()), 
                    transaction.getYear(), 
                    transaction.getPrice(), 
                    transaction.getCategory(), 
                    transaction.getRecurring() ? "Yes" : "No", 
                    transaction.getEveryMonth()});
            }
        }
    }
    
    
    private void writeTransactions() {
        //sorts the transactions
        transactions.sort(Comparator.comparing(Transaction::getYear)
                .thenComparing(Transaction::getMonth)
                .thenComparing(Transaction::getPrice)
                .thenComparing(Transaction::getCategory)
                .thenComparing(Transaction::getRecurring)
                .thenComparing(Transaction::getEveryMonth));
        
        //writes the sorted list to disk
        FileIO.writeTransactions(transactions);
    }
    
    
